FUNCTION APPM (k1, k2, k3)
!----------------------------------------------------------------------
  use precision
  use spinor
  implicit none
  COMPLEX (pr) :: APPM
  INTEGER k1, k2, k3

  APPM=-RAC8*S (1, 2)*S (1, k3)**2/S (1, k1)/S (1, k2)/S (2, k1)/S (2, k2)

  RETURN
END FUNCTION APPM
