  COMPLEX (pr) :: BMMM
  INTEGER k1, k2, k3

  BMMM=-RAC8*T (1, 2)* ( &
       + (S (k1, 1)*S (k2, k3))**2 &
       + (S (k2, 1)*S (k1, k3))**2 &
       + (S (k3, 1)*S (k1, k2))**2 &
       )

  RETURN
END FUNCTION BMMM
