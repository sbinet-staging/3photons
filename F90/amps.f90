!----------------------------------------------------------------------
FUNCTION BPPP (k1, k2, k3)
!----------------------------------------------------------------------
  use precision
  use spinor
  implicit none
  COMPLEX (pr) :: BPPP
  INTEGER k1, k2, k3

  BPPP=-RAC8*S (1, 2)* ( &
       + (T (k1, k2)*T (k3, 2))**2 &
       + (T (k1, k3)*T (k2, 2))**2 &
       + (T (k2, k3)*T (k1, 2))**2 &
       )

  RETURN
END FUNCTION BPPP
!----------------------------------------------------------------------

