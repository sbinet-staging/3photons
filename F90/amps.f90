  use spinor
  implicit none
  COMPLEX (pr) :: APMM
  INTEGER k1, k2, k3

  APMM=-RAC8*T (1, 2)*T (2, k1)**2/T (2, k2)/T (2, k3)/T (1, k2)/T (1, k3)

  RETURN
END FUNCTION APMM
