C----------------------------------------------------------------------
      double complex FUNCTION APPM(k1,k2,k3)
C----------------------------------------------------------------------
!     $ use OMP_LIB
      IMPLICIT NONE
      INTEGER k1,k2,k3
      INCLUDE 'spinor.for'

      APPM=-RAC8*S(1,2)*S(1,k3)**2/S(1,k1)/S(1,k2)/S(2,k1)/S(2,k2)

      RETURN
      END
C----------------------------------------------------------------------

C----------------------------------------------------------------------
      double complex FUNCTION APMM(k1,k2,k3)
C----------------------------------------------------------------------
!     $ use OMP_LIB
      IMPLICIT NONE
      INTEGER k1,k2,k3
      INCLUDE 'spinor.for'

      APMM=-RAC8*T(1,2)*T(2,k1)**2/T(2,k2)/T(2,k3)/T(1,k2)/T(1,k3)

      RETURN
      END
C----------------------------------------------------------------------

C----------------------------------------------------------------------
      double complex FUNCTION BPPM(k1,k2,k3)
C----------------------------------------------------------------------
!     $ use OMP_LIB
      IMPLICIT NONE
      INTEGER k1,k2,k3
      INCLUDE 'spinor.for'

      BPPM=-RAC8*T(1,2)*(T(k1,k2)*S(k3,1))**2

      RETURN
      END
C----------------------------------------------------------------------

C----------------------------------------------------------------------
      double complex FUNCTION BPMM(k1,k2,k3)
C----------------------------------------------------------------------
!     $ use OMP_LIB
      IMPLICIT NONE
      INTEGER k1,k2,k3
      INCLUDE 'spinor.for'

      BPMM=-RAC8*S(1,2)*(T(k1,2)*S(k2,k3))**2

      RETURN
      END
C----------------------------------------------------------------------

C----------------------------------------------------------------------
      double complex FUNCTION BPPP(k1,k2,k3)
C----------------------------------------------------------------------
!     $ use OMP_LIB
      IMPLICIT NONE
      INTEGER k1,k2,k3
      INCLUDE 'spinor.for'

      BPPP=-RAC8*S(1,2)*(
     + + (T(k1,k2)*T(k3,2))**2
     + + (T(k1,k3)*T(k2,2))**2
     + + (T(k2,k3)*T(k1,2))**2
     +     )

      RETURN
      END
C----------------------------------------------------------------------

C----------------------------------------------------------------------
      double complex FUNCTION BMMM(k1,k2,k3)
C----------------------------------------------------------------------
!     $ use OMP_LIB
      IMPLICIT NONE
      INTEGER k1,k2,k3
      INCLUDE 'spinor.for'

      BMMM=-RAC8*T(1,2)*(
     +     + (S(k1,1)*S(k2,k3))**2
     +     + (S(k2,1)*S(k1,k3))**2
     +     + (S(k3,1)*S(k1,k2))**2
     +     )

      RETURN
      END
